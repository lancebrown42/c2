#include "CDog.h"


// --------------------------------------------------------------------------------
// Name: SetValue
// Abstract: Set the value
// --------------------------------------------------------------------------------
void CDog::SetValue(int intNewValue)
{
	// Boundary check
	if (intNewValue < 0)
	{
		// Clip to prevent negative value
		intNewValue = 0;
	}

	m_intValue = intNewValue;
}


// --------------------------------------------------------------------------------
// Name: GetValue
// Abstract: Get the Value
// --------------------------------------------------------------------------------
int CDog::GetValue()
{
	return m_intValue;
}


